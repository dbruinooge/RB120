class CircularQueue
  def initialize(buffer_size)
    @queue = []
    @max_buffer = buffer_size
  end
  
  def enqueue(element)
    @queue.shift if full?
    @queue << element
  end
  
  def dequeue
    @queue.shift
  end
  
  def full?
    @queue.size == @max_buffer
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
queue.enqueue(6)
queue.enqueue(7)
puts queue.dequeue == 5

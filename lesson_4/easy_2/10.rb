# What are the benefits of using Object Oriented Programming in Ruby? Think of as many as you can.

=begin

1. It makes the code easier to maintain, since the program would otherwise be one large dependency.
2. If parts of the program are encapsulated (walled off), it allows data to be protected.
3. It allows code to be reused in other programs.
4. It allows real-life concepts (nouns and verbs) to be mapped to objects (classes) and methods.
5. It makes it easier for the developer to understand large and complex programs.

=end
